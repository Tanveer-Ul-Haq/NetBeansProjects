    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String Name = jTextField1.getText();
        String Fname = jTextField2.getText();
        String CNIC = jTextField3.getText();
        String Email = jTextField4.getText();
        String Phone = jTextField5.getText();
        String Address = jTextField6.getText();
        String University = jTextField7.getText();
        String Room = jComboBox1.getSelectedItem().toString();
        
         try{
            ResultSet r=conn.createStatement().executeQuery("Select * from new_Student where ID_No like '%"+CNIC+"%'");
            if(r.next())
            {
                JOptionPane.showMessageDialog(this,"This CNIC Number is already exist. \n Change your CNIC Number.");
                jTextField3.setText("");
            }
            else
            {
                 conn.createStatement().executeQuery("insert into new_student(Name, Fname, ID_No, Email_id, Phone_No, Address, University_name, Room_No) values('"+Name+"','"+Fname+"','"+CNIC+"','"+Email+"','"+Phone+"','"+Address+"','"+University+"','"+Room+"')"); 
                 conn.createStatement().executeQuery("update rooms set Booked='Yes' where Room_No= "+Room+"");
                 ResultSet rs=conn.createStatement().executeQuery("Select Room_No from rooms where Room_Status= 'Yes' and Booked ='No'");
            
            while(rs.next())
            {
                  String numb =rs.getString(1);
                  jComboBox1.addItem(numb);
            }            
            JOptionPane.showMessageDialog(this,"Student Registered Successfully","",JOptionPane.PLAIN_MESSAGE);
            }
             
         }
         catch(Exception e)
                    {
                 //       JOptionPane.showMessageDialog(this,"Error","",JOptionPane.ERROR_MESSAGE);
                    System.out.println("   Error");
                    } 
    }//GEN-LAST:event_jButton1ActionPerformed

